package com.jtzh.pojo;

public class CountrysideBuildCount {
	
	private Integer countrysideId;
	private String countryside;
	private String count;
	
	public Integer getCountrysideId() {
		return countrysideId;
	}
	public void setCountrysideId(Integer countrysideId) {
		this.countrysideId = countrysideId;
	}
	public String getCountryside() {
		return countryside;
	}
	public void setCountryside(String countryside) {
		this.countryside = countryside;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}

}
