package com.jtzh.responsevo;

import com.jtzh.entity.SecurityInfReceive;

public class SecurityInfReceiveVo extends SecurityInfReceive {
	private String title;
	private String content;
	private String enclosure;
	private String nickName;
	private String realName;
	private String depName;
	private String vilName;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getEnclosure() {
		return enclosure;
	}
	public void setEnclosure(String enclosure) {
		this.enclosure = enclosure;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getRealName() {
		return realName;
	}
	public void setRealName(String realName) {
		this.realName = realName;
	}
	public String getDepName() {
		return depName;
	}
	public void setDepName(String depName) {
		this.depName = depName;
	}
	public String getVilName() {
		return vilName;
	}
	public void setVilName(String vilName) {
		this.vilName = vilName;
	}
	
}
